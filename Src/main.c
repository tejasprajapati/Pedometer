int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_ADC_Init();

  /* USER CODE BEGIN 2 */
	//Init LCD
	ssd1306_Init();
	HAL_Delay(50);
	
	//Config the HDC1080 to perform acquisition separately
	HAL_Delay(15);
	buffer[0]=0x02; //Pointer
	buffer[1]=0;	//MSB byte
	buffer[3]=0; 	//LSB byte
	HAL_I2C_Master_Transmit(&hi2c1,0x40<<1,buffer,3,100);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
		//Trigger Temperature measurement
		buffer[0]=0x00;
		HAL_I2C_Master_Transmit(&hi2c1,0x40<<1,buffer,1,100);
		
		HAL_Delay(20);
		
		HAL_I2C_Master_Receive(&hi2c1,0x40<<1,buffer,2,100);
		//buffer[0] : MSB data
		//buffer[1] : LSB data
		
		rawT = buffer[0]<<8 | buffer[1];	//combine 2 8-bit into 1 16bit
		
		Temperature = ((float)rawT/65536)*165.0 -40.0;

		//Trigger Humidity measurement
		buffer[0]=0x01;
		HAL_I2C_Master_Transmit(&hi2c1,0x40<<1,buffer,1,100);
		
		HAL_Delay(20);
		
		HAL_I2C_Master_Receive(&hi2c1,0x40<<1,buffer,2,100);
		//buffer[0] : MSB data
		//buffer[1] : LSB data
		
		rawH = buffer[0]<<8 | buffer[1];	//combine 2 8-bit into 1 16bit
		
		Humidity = ((float)rawH/65536)*100.0;
		
                
                Temperature = 25;
                Humidity = 90;
		
		//Display temperature and humidity on LCD
		sprintf(lcd_buf,"Temperature:%.1f",Temperature); //convert temperature to string
		ssd1306_SetCursor(0,0);	//set cursor position x=0, y=0
		ssd1306_WriteString(lcd_buf,Font_7x10,White);
		
		sprintf(lcd_buf,"Humidity:%.1f",Humidity); //convert humidity to string
		ssd1306_SetCursor(0,15);	//set cursor position x=0,y=15
		ssd1306_WriteString(lcd_buf,Font_7x10,White);
		
                ssd1306_SetCursor(0,30);	//set cursor position x=0,y=15
		ssd1306_WriteString(lcd_buf,Font_7x10,White);

                ssd1306_SetCursor(0,45);	//set cursor position x=0,y=15
		ssd1306_WriteString(lcd_buf,Font_7x10,White);
                
		ssd1306_UpdateScreen();	//Update the LCD
		//End of LCD display
		
		HAL_Delay(100);
  }
  /* USER CODE END 3 */

}

/** System Clock Configuration
*/
void SystemClock_Config(void)
{

  RCC_OscInitTypeDef RCC_OscInitStruct;
  RCC_ClkInitTypeDef RCC_ClkInitStruct;
  RCC_PeriphCLKInitTypeDef PeriphClkInit;

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSI|RCC_OSCILLATORTYPE_HSI14;
  RCC_OscInitStruct.HSIState = RCC_HSI_ON;
  RCC_OscInitStruct.HSI14State = RCC_HSI14_ON;
  RCC_OscInitStruct.HSICalibrationValue = 16;
  RCC_OscInitStruct.HSI14CalibrationValue = 16;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_ON;
  RCC_OscInitStruct.PLL.PLLSource = RCC_PLLSOURCE_HSI;
  RCC_OscInitStruct.PLL.PLLMUL = RCC_PLL_MUL12;
  RCC_OscInitStruct.PLL.PREDIV = RCC_PREDIV_DIV1;
  if (HAL_RCC_OscConfig(&RCC_OscInitStruct) != HAL_OK)
  {
    Error_Handler();
  }

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK|RCC_CLOCKTYPE_SYSCLK
                              |RCC_CLOCKTYPE_PCLK1;
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_PLLCLK;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV1;

  if (HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_1) != HAL_OK)
  {
    Error_Handler();
  }

  PeriphClkInit.PeriphClockSelection = RCC_PERIPHCLK_I2C1;
  PeriphClkInit.I2c1ClockSelection = RCC_I2C1CLKSOURCE_HSI;
  if (HAL_RCCEx_PeriphCLKConfig(&PeriphClkInit) != HAL_OK)
  {
    Error_Handler();
  }

    /**Configure the Systick interrupt time 
    */
  HAL_SYSTICK_Config(HAL_RCC_GetHCLKFreq()/1000);

    /**Configure the Systick 
    */
  HAL_SYSTICK_CLKSourceConfig(SYSTICK_CLKSOURCE_HCLK);

  /* SysTick_IRQn interrupt configuration */
  HAL_NVIC_SetPriority(SysTick_IRQn, 0, 0);
}

